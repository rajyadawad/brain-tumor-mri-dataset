"""
evaluate.py
===========
Independent evaluation of the released checkpoints on the held-out test set.

Run as a script::

    python -m src.evaluate

It loads the saved ``.keras`` models, predicts on ``Dataset/Testing``, and writes
``results/metrics/saved_model_eval.json`` — the single source of truth for the
numbers quoted in the README. Because it evaluates the *files on disk* (not an
in-memory model), a green run proves the released checkpoints match the reported
metrics (audit item H3).
"""

from __future__ import annotations

import argparse
import json
import os

import numpy as np

from . import config
from .calibration import compute_ece
from .data_loader import build_datasets
from .utils import get_logger, load_model, set_seed

logger = get_logger(__name__)


def _macro_metrics(y_true: np.ndarray, y_pred: np.ndarray) -> dict[str, float]:
    from sklearn.metrics import accuracy_score, f1_score, precision_score, recall_score
    return {
        "test_accuracy": float(accuracy_score(y_true, y_pred)),
        "macro_precision": float(precision_score(y_true, y_pred, average="macro", zero_division=0)),
        "macro_recall": float(recall_score(y_true, y_pred, average="macro", zero_division=0)),
        "macro_f1": float(f1_score(y_true, y_pred, average="macro", zero_division=0)),
    }


def evaluate_model(model, test_ds, y_true: np.ndarray) -> dict[str, float]:
    """Run a model over ``test_ds`` and return accuracy/precision/recall/F1/ECE."""
    probs = model.predict(test_ds, verbose=0)
    y_pred = probs.argmax(axis=1)
    conf = probs.max(axis=1)
    correct = (y_pred == y_true).astype(float)

    metrics = _macro_metrics(y_true, y_pred)
    metrics["mean_confidence"] = float(conf.mean())
    metrics["ece_10bin"] = round(compute_ece(conf, correct).ece, 4)
    metrics["n_test_errors"] = int((y_pred != y_true).sum())
    return {k: (round(v, 4) if isinstance(v, float) else v) for k, v in metrics.items()}


def main() -> None:
    parser = argparse.ArgumentParser(description="Evaluate released checkpoints on the test set.")
    parser.add_argument("--out", default=os.path.join(config.METRICS_DIR, "saved_model_eval.json"))
    args = parser.parse_args()

    set_seed()
    import tensorflow as tf

    _, _, test_ds, class_names = build_datasets()
    y_true = np.concatenate([y.numpy() for _, y in test_ds])

    results = {}
    for label, path in [("efficientnet", config.EFFNET_PATH), ("cnn", config.CNN_PATH)]:
        logger.info("Evaluating %s (%s)", label, path)
        model = load_model(path)
        results[os.path.relpath(path, config.PROJECT_ROOT).replace("\\", "/")] = \
            evaluate_model(model, test_ds, y_true)

    payload = {
        "description": (
            "Independent evaluation of the RELEASED model checkpoints in models/ on "
            "Dataset/Testing (1,600 images). Generated by src/evaluate.py. Numbers may "
            "differ at the ~0.5pt level from the notebook's in-session results due to "
            "non-determinism; the two-stage fine-tuning saves the fine-tuned model "
            "explicitly so the released EfficientNet is the model that is evaluated."
        ),
        "environment": {
            "tensorflow": tf.__version__,
            "image_size": config.IMAGE_SIZE,
            "batch_size": config.BATCH_SIZE,
            "seed": config.SEED,
        },
        "classes": list(class_names),
        "released_checkpoints": results,
    }

    os.makedirs(os.path.dirname(args.out), exist_ok=True)
    with open(args.out, "w", encoding="utf-8") as fh:
        json.dump(payload, fh, indent=2)
    logger.info("Wrote %s", args.out)
    print(json.dumps(results, indent=2))


if __name__ == "__main__":
    main()
